"""
13. Crie uma variável com um número grande e utilize operadores aritméticos para transformá-lo.
"""
numero = 25

print("Número inicial:", numero)

numero = numero + 100
print("Após somar 100:", numero)

numero = numero * 2
print("Após multiplicar por 2:", numero)

numero = numero // 3
print("Após divisão inteira por 3:", numero)

numero = numero / 3
print('Após divisão simples por 3: ', numero)

numero = numero % 5
print("Resto da divisão por 5:", numero)

numero = numero ** 2
print("Elevado ao quadrado:", numero)
