"""
9. Crie um programa que receba dois números do usuário e exiba todas as 4 operações matemáticas básicas entre eles.
"""
a = int(input("Digite um numero: "))
b = int(input("Digite outro numero: "))
print(f'Soma: [{a+b}]')
print(f'Subtração: [{a-b}]')
print(f'Multiplicação: [{a*b}]')
print(f'Divisão: [{a/b}]')